create table tusers (
  id INT auto_increment primary key,
  login VARCHAR(255) not null,
  password VARCHAR(255) not null,
  salt VARCHAR(255) not null,
  firstname VARCHAR(255) not null,
  lastname VARCHAR(255) not null,
  profile INT not null,
  mail VARCHAR(255) not null,
  phone VARCHAR(255) not null,
  mobile VARCHAR(255) not null,
  fax VARCHAR(255) not null,
  `function` VARCHAR(255) not null,
  company INT not null,
  address1 VARCHAR(255) not null,
  address2 VARCHAR(255) not null,
  zip VARCHAR(255) not null,
  city VARCHAR(255) not null,
  custom1 VARCHAR(255) not null,
  custom2 VARCHAR(255) not null,
  disable INT not null,
  chgpwd INT not null,
  last_login DATETIME not null,
  last_pwd_chg DATE not null,
  auth_attempt INT not null,
  ip VARCHAR(255) not null,
  skin VARCHAR(255) not null,
  default_ticket_state VARCHAR(255) not null,
  dashboard_ticket_order VARCHAR(255) not null,
  limit_ticket_number INT not null,
  limit_ticket_days INT not null,
  limit_ticket_date_start DATE not null,
  language VARCHAR(255) not null default 'fr_FR',
  ldap_guid VARCHAR(255) not null,
  ldap_sid VARCHAR(255) not null,
  azure_ad_id VARCHAR(255) not null,
  planning_color VARCHAR(255) not null
);

